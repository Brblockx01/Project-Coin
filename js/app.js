const signer = provider.getSigner();
const BCoinContract = new ethers.Contract(contractAddress, contractAbi, signer);


const connectWalletBtn = document.getElementById("connect-wallet-btn");

connectWalletBtn.addEventListener("click", async () => {
    if(typeof window.ethereum !== "undefined") {
        await ethereum.request({ method: "eth_requestAccounts" });
    } else {
        alert("metamask not installed!")
    }
});

const coinName = document.getElementById("coin-name");

const nameFunction = async () => {
    coinName.innerText = await BCoinContract.symbol();
}
nameFunction();

const mintCoinsBtn = document.getElementById("mint-coins-btn");
const mintAmountValue = document.getElementById("mint-amount-value");
const amountAlert = document.getElementById("amount-alert");

const checkBalanceInput = document.getElementById("check-balance-input");
const checkBalanceBtn = document.getElementById("check-balance-btn");
const checkBalanceDisplay = document.getElementById("check-balance-display");

mintCoinsBtn.addEventListener("click", async () => {
    const mintAmountInputValue = mintAmountValue.value;
    if(mintAmountInputValue < 1) {
        amountAlert.innerText = "Please enter valid amount."
    } else {
        let coinMinted = await BCoinContract.mint(mintAmountInputValue);
        console.log(coinMinted.from);
        console.log(coinMinted);
    }
})

